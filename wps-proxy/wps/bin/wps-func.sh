
# HEADER
# ---------------------------------------------------------------------------------

wps_header() {
	echo -e "\033[0;30m
-----------------------------------------------------
\033[0;34m  Submarine\033[0;37m - $1\033[0;30m
-----------------------------------------------------
\033[0m"
}

# CHMOD
# ---------------------------------------------------------------------------------

wps_chmod() { 

	find $run -type f -exec chmod +x {} \;
	find $run -type d -exec chmod 755 {} \;
}

